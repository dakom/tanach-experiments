//! Abstraction over *where* raw dataset bytes come from.
//!
//! Today the only implementation reads from the local `media/` directory
//! ([`FileDataSource`]). The [`DataSource`] trait exists so we can later drop in
//! a URL-backed source (e.g. fetching `https://host/tanach/letters.json`)
//! without touching any calling code — every consumer depends on the trait, not
//! on the filesystem.

use anyhow::{Context, Result};
use std::fs;
use std::path::PathBuf;

/// A source of raw dataset bytes, addressed by a `/`-separated relative path
/// such as `"tanach/letters.json"` or `"tanach/separate-books/yonah.json"`.
pub trait DataSource {
    fn load(&self, resource: &str) -> Result<Vec<u8>>;
}

/// So callers can hold a `DataSource` chosen at runtime (`FileDataSource` vs
/// `EmbeddedDataSource`) behind a single boxed type.
impl DataSource for Box<dyn DataSource> {
    fn load(&self, resource: &str) -> Result<Vec<u8>> {
        (**self).load(resource)
    }
}

/// Loads resources from a base directory on the local filesystem.
pub struct FileDataSource {
    base: PathBuf,
}

impl FileDataSource {
    pub fn new(base: impl Into<PathBuf>) -> Self {
        Self { base: base.into() }
    }
}

impl DataSource for FileDataSource {
    fn load(&self, resource: &str) -> Result<Vec<u8>> {
        let path = self.base.join(resource);
        fs::read(&path).with_context(|| {
            format!(
                "failed to read resource `{resource}` from {}",
                path.display()
            )
        })
    }
}

/// Serves the dataset from bytes baked into the binary at compile time
/// (gzip-compressed; see `build.rs`). This is what makes the release binary
/// self-contained and relocatable — no `media/` directory required.
#[cfg(feature = "embed-data")]
pub struct EmbeddedDataSource;

#[cfg(feature = "embed-data")]
mod embedded {
    // Generated by build.rs: `EMBEDDED: &[(&str, &[u8])]` mapping each resource
    // path to its gzip-compressed bytes.
    include!(concat!(env!("OUT_DIR"), "/embedded.rs"));
}

#[cfg(feature = "embed-data")]
impl DataSource for EmbeddedDataSource {
    fn load(&self, resource: &str) -> Result<Vec<u8>> {
        use std::io::Read;

        let compressed = embedded::EMBEDDED
            .iter()
            .find(|(name, _)| *name == resource)
            .map(|(_, bytes)| *bytes)
            .with_context(|| format!("embedded resource `{resource}` not found"))?;

        let mut out = Vec::new();
        flate2::read::GzDecoder::new(compressed)
            .read_to_end(&mut out)
            .with_context(|| format!("failed to decompress embedded resource `{resource}`"))?;
        Ok(out)
    }
}

